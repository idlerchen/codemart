package com.fenbi.codemart.service;

import java.util.List;

import com.fenbi.codemart.bean.RoleType;

public interface RoleTypeService {
	
	/**
	 * 处理查询所有的角色类型的业务逻辑
	 * @return 所有角色类型的List集合
	 */
	public List<RoleType> getRoleTypes();
	
	/**
	 * 处理根据父角色类型id查询所有的子角色类型的业务逻辑
	 * @param roleTypeId 父角色类型id
	 * @return 所有子角色类型的List集合
	 */
	public List<RoleType> getRoleTypesByRoleTypeId(int roleTypeId);

}
