package com.fenbi.codemart.bean;

/**
 * 项目状态信息的实体类，封装item_state表的信息
 *
 */
public class ItemState {

	private int id;
	private String name;

	public ItemState() {
		super();
	}

	public ItemState(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "ItemState [id=" + id + ", name=" + name + "]";
	}

}
